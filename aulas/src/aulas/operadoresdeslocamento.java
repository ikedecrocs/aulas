package aulas;

public class operadoresdeslocamento {

	public static void main(String[] args) {
	//deslocamento a direita
		int i1 = 192 >> 1;
		int i2 = 192 >> 7;
		
		int i3 = -192 >> 1;
		int i4 = -192 >> 7;
		
	//deslocamento a esquerda
		int i5 = 192 << 1;
		int i6 = 192 << 7;
		
		int i7 = -192 << 1;
		int i8 = -192 << 7;
		
	//deslocamento a direita,sem sinal
		int i9 = 192 >>> 1;
		int i10 = 192 >>> 7;
		
		int i11 = -192 >>> 1;
		int i12 = -192 >>> 7;
	}

}
